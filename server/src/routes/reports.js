// Helper: Setup Standard Excel Styles
const formatWorksheet = (worksheet, title, user, filters = {}) => {
  worksheet.addRow(['NKB IT Management System - Analytical Report']);
  worksheet.addRow([`Report Title: ${title}`]);
  worksheet.addRow([`Generated by: ${user.username} on ${new Date().toLocaleString()}`]);
  
  if (Object.keys(filters).length > 0) {
    const filterStrings = Object.entries(filters)
      .filter(([_, v]) => v !== undefined && v !== '')
      .map(([k, v]) => `${k}: ${v}`)
      .join(' | ');
    if (filterStrings) worksheet.addRow([`Filters Applied: ${filterStrings}`]);
  }
  
  worksheet.addRow([]); // empty space

  worksheet.getRow(1).font = { bold: true, size: 14 };
  worksheet.getRow(2).font = { italic: true };
};

const formatHeaders = (worksheet, rowNum) => {
  const row = worksheet.getRow(rowNum);
  row.font = { color: { argb: 'FFFFFFFF' }, bold: true };
  row.fill = {
    type: 'pattern',
    pattern: 'solid',
    fgColor: { argb: 'FF0F172A' } // Navy blue
  };
  row.alignment = { vertical: 'middle', horizontal: 'center' };
};

const autoFitColumns = (worksheet) => {
  worksheet.columns.forEach(column => {
    let maxLen = 0;
    column.eachCell({ includeEmpty: true }, cell => {
      const valStr = cell.value ? cell.value.toString() : '';
      if (valStr.length > maxLen) maxLen = valStr.length;
    });
    column.width = Math.max(maxLen + 2, 10);
  });
};

