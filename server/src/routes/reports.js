// 1. ASSET MASTER LIST REPORT
// ==========================================
router.get('/assets', authenticateToken, requirePermission('reports.view'), async (req, res) => {
  const { categoryId, status, condition, startDate, endDate } = req.query;

  try {
    const query = db('assets')
      .join('asset_categories', 'assets.category_id', 'asset_categories.id')
      .leftJoin('departments', 'assets.department_id', 'departments.id')
      .leftJoin('employees', 'assets.employee_id', 'employees.id')
      .select(
        'assets.asset_code', 'assets.name', 'assets.brand', 'assets.model', 'assets.serial_number',
        'asset_categories.name as category_name', 'assets.condition', 'assets.status',
        'departments.name as department_name',
        db.raw("concat(employees.first_name, ' ', employees.last_name) as employee_name"),
        'assets.purchase_price', 'assets.purchase_date', 'assets.warranty_end_date'
      );

    if (categoryId) query.where('assets.category_id', categoryId);
    if (status) query.where('assets.status', status);
    if (condition) query.where('assets.condition', condition);
    if (startDate) query.where('assets.purchase_date', '>=', startDate);
    if (endDate) query.where('assets.purchase_date', '<=', endDate);

    const data = await query.orderBy('assets.asset_code', 'asc');
    return res.json({ success: true, data });
  } catch (err) {
    logger.error(`Get assets report error: ${err.message}`);
    return res.status(500).json({ success: false, message: 'Failed to generate asset report.' });
  }
});

